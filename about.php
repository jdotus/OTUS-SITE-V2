                            themselves the Data Images Plus Automation Phil's. Inc. (DIPAPI) on one hand; while the allied A4 Copy 
                            Systems, Inc. (A4CSI) and FDSI-QC had opted in selling their stocks to FDSI on the other hand. The
                            eleven remaining members continued the FDSI business in the brink of bankruptcy, with zero customer 
                            base. and ninety percent of employees gone. </div>
