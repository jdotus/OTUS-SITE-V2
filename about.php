                      </h2>
                      <div id="flush-collapseThree" class="accordion-collapse collapse" data-bs-parent="#accordionFlushExample">
                        <div class="accordion-body">In <span>2006</span>, the president of FDSI concurrently organized the Otus Copy Systems, Inc. (OCSI) to steer the
                            remaining employees into adopting a new strategy, while going to a process of winding-up the former 
                            business. It was during that same time when OCSI had been approved to become an Authorized Channel
                            Partner of Fuji Xerox Phils., Inc. (FXPI). </div>
                      </div>
                    </div>
                    <div class="accordion-item">
                        <h2 class="accordion-header">
                          <button class="accordion-button collapsed d-block text-center" type="button" data-bs-toggle="collapse" data-bs-target="#flush-collapseFour" aria-expanded="false" aria-controls="flush-collapseFour">
                            2008
                          </button>
                        </h2>
                        <div id="flush-collapseFour" class="accordion-collapse collapse" data-bs-parent="#accordionFlushExample">
                          <div class="accordion-body">In <span>2008</span>, OCSI was re-structured by buying-out stocks of the "remnants of the old", those directors who
                            refused to cooperate on the novel concept of taking in One (1) brand "FUJI XEROX" - One (1) sector
                            "Government" - One (1) approach "Managed Services" (a.k.a. 1-1-1 Strategy) of dealership, as opposed 
                            to the traditional multiple-brand, box-pushing and off-the-shelf-selling-centric approach common to
                            many dealers at that time. It was during the same period when OCSI - FDSI was reduced to 7 members. </div>
                        </div>
